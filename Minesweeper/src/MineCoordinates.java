
public class MineCoordinates {
	
	private double xPos;
	private double yPos;
	
	
	public MineCoordinates ( double x, double y){
		
		this.xPos= x;
		this.yPos = y;
		
		}


	public double getxPos() {
		return xPos;
	}


	public void setxPos(double xPos) {
		this.xPos = xPos;
	}


	public double getyPos() {
		return yPos;
	}


	public void setyPos(double yPos) {
		this.yPos = yPos;
	}
	
	

}
